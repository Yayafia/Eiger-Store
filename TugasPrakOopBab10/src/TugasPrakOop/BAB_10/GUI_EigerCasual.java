    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblData.getSelectedRow();
        if (selectedRow != -1) {
        // Ambil data dari form input
        String nama = txtNama.getText().trim();
        String lokasi = txtLokasi.getText().trim();
        String kelamin = cmbJk.getSelectedItem().toString();
        String warna = txtWarna.getText().trim();
        String tambahan = txtInfo.getText().trim();

        // Validasi input
        if (nama.isEmpty() || lokasi.isEmpty() || warna.isEmpty() || tambahan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Buat objek dan ambil promo
        EigerCasualStore store = new EigerCasualStore(nama, lokasi, kelamin, warna, tambahan);
        String promo = store.infoPromo();

        // Update data di tabel
        tableModel.setValueAt(nama, selectedRow, 0);
        tableModel.setValueAt(lokasi, selectedRow, 1);
        tableModel.setValueAt(kelamin, selectedRow, 2);
        tableModel.setValueAt(warna, selectedRow, 3);
        tableModel.setValueAt(tambahan, selectedRow, 4);
        tableModel.setValueAt("Tidak", selectedRow, 5); // Casual Store = Tidak
        tableModel.setValueAt(promo, selectedRow, 6);

        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diperbarui.", "Tidak Ada Baris Terpilih", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnUbahActionPerformed

