    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText();
        String lokasi = txtLokasi.getText();
        String kelamin = cmbJk.getSelectedItem().toString();
        String warna = txtWarna.getText();
        String info = txtInfo.getText();

        if (nama.isEmpty() || lokasi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Lokasi harus diisi!");
            return;
        }

        EigerCasualStore store = new EigerCasualStore(nama, lokasi, kelamin, warna, info);
        String promo = store.infoPromo();
        tableModel.addRow(new Object[]{store.getNamaToko(), store.getLokasi(), kelamin, warna, info, "Tidak", promo});
        JOptionPane.showMessageDialog(this, store.tampilkanInfoToko());
        clearForm();
    }//GEN-LAST:event_btnTambahActionPerformed

